    /**
     * 获取两个数之间的浮点随机值
     *
     * @param $min
     * @param $max
     *
     * @return float
     */
    public static function randomFloat($min = 0, $max = 1)
    {
        return round(($min + mt_rand() / mt_getrandmax() * ($max - $min)) * 10) / 10;
    }

